module rom(
    input wire [31:0] addr,    // 接收来自 PC 的地址
    output reg [31:0] inst     // 输出读取到的指令
);

    // 参数化 ROM 大小
    parameter ROM_SIZE = 32;
    reg [31:0] rom_mem [0:ROM_SIZE-1]; 
    integer i;

    initial begin
        // 默认全部初始化为 NOP (addi x0, x0, 0)
        for (i = 0; i < ROM_SIZE; i = i + 1) begin
            rom_mem[i] = 32'h00000013; 
        end

        // =========================================================================
        //                 🚀 五级流水线地狱级全功能压力测试集 🚀
        // =========================================================================

        // --- 阶段 1：x0 硬编码安全测试 & 基础 RAW 前推 ---
        // [0x00] ADDI x0, x0, 10    -> 写入 x0。验证：前推模块绝不能把 10 错推给后续指令
        rom_mem[0] = 32'h00a00013; 
        // [0x04] ADD  x4, x0, x0    -> x4 = x0 + x0 = 0。若前推了 x0 会导致 x4 != 0 (Bug!)
        rom_mem[1] = 32'h00000233; 
        // [0x08] ADDI x3, x4, 20    -> x3 = 0 + 20 = 20 (0x14)。测试从 EX/MEM 到 EX 的常规前推
        rom_mem[2] = 32'h01420193; 

        // --- 阶段 2：Load-Use 冲突与解堵测试 ---
        // [0x0C] SW   x3, 16(x0)    -> RAM[16] = 20 (0x14)。把刚才算出的值存入内存
        rom_mem[3] = 32'h00302823; 
        // [0x10] LW   x5, 16(x0)    -> x5 = RAM[16] = 20 (0x14)。
        rom_mem[4] = 32'h01002283; 
        // [0x14] ADDI x6, x5, 5     -> 【核心测试 1】Load-Use 冲突！
        //                              必须在 ID 阶段 Stall 一拍，待 LW 走完 MEM 阶段再前推给 EX。
        //                              正确解堵后：x6 = 20 + 5 = 25 (0x19)
        rom_mem[5] = 32'h00528313; 

        // --- 阶段 3：多重连续 RAW 数据依赖测试 ---
        // [0x18] ADD  x7, x6, x3    -> 【核心测试 2】多重依赖。x6(25) 来自上句（EX 前推），x3(20) 来自更早（MEM 前推）
        //                              此时前推模块必须同时捞对这两个寄存器！x7 = 25 + 20 = 45 (0x2D)
        rom_mem[6] = 32'h003303b3; 

        // --- 阶段 4：Control 控制冲突 (Branch 冲刷测试) ---
        // [0x1C] BNE  x7, x0, 16    -> 【核心测试 3】因为 x7(45) != 0，分支 100% 采纳！
        //                              PC 应该跳转到 0x1C + 16 = 0x2C。
        rom_mem[7] = 32'h00039863; 
        // [0x20] ADDI x7, x0, 999   -> 【翻车点 A】这句在流水线阴影区，必须被 Flush！x7 绝对不能变成 999
        rom_mem[8] = 32'h3e700393; 
        // [0x24] ADDI x6, x0, 888   -> 【翻车点 B】这句也必须被 Flush！x6 绝对不能变成 888
        rom_mem[9] = 32'h37800313; 
        // [0x28] NOP                -> 保护空挡
        rom_mem[10] = 32'h00000013; 

        // --- 阶段 5：Branch-then-Load 保守策略特殊测试 ---
        // 跳转目的地：0x2C
        // [0x2C] BEQ  x0, x0, 8     -> 【核心测试 4】EX 是分支（满足满足条件），下一句 ID 是 Load！
        //                              触发你的 handler 中特殊的“先 branch 后 load”保守机制。
        //                              分支跳转成功，跳到 0x2C + 8 = 0x34。
        rom_mem[11] = 32'h00000463; 
        // [0x30] LW   x1, 16(x0)    -> 【翻车点 C】处于 Branch 后的 Load。
        //                              即使它触发了你的 2 周期保守计数器，但由于上句分支成立，
        //                              它最终应当伴随整个译码阶段被 Flush 掉！
        rom_mem[12] = 32'h01002083; 

        // --- 阶段 6：终点站与结果验证 ---
        // 跳转目的地：0x34
        // [0x34] ADDI x8, x0, 1     -> 终点标记。x8 = 1 代表成功跑完全程！
        rom_mem[13] = 32'h00100413; 

    end

    // 异步读取 
    always @(*) begin
        if (addr[6:2] < ROM_SIZE) begin
            inst = rom_mem[addr[6:2]];
        end else begin
            inst = 32'h00000013; // 超出范围返回 NOP
        end
    end
endmodule